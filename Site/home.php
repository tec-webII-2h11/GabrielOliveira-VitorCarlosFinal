                MotoGP é o Campeonato Mundial de motociclismo premier; uma série de dezoito corridas que visita quatorze países, quatro continentes e com cobertura televisiva global. Dez nacionalidades dos pilotos mais habilidosos do mundo em uma linha de largada  com protótipos de motos de ponta disponibilizadas por 5 fabricantes; Aprilia, Ducati, Honda, Suzuki e Yamaha. </p>
<p class="sobre2">
A MotoGP foi estabelecido como um Mundial pela FIM (Federação Internacional de Motociclismo) em 1949. É o mais antigo campeonato de automobilismo no mundo e com três classes de corrida que vão para a pista em um típico fim de semana Grand Prix. Anteriormente chamado '500cc ", o campeonato sofreu uma mudança em 2002, com os novos regulamentos técnicos permitindo a introdução de máquinas de quatro tempos e aumentando a capacidade do motor para 990cc, tornando-se assim a MotoGP. A partir de 2007 as regras foram alteradas, o que limita a capacidade do motor para 800cc, e mais uma vez a partir de 2012, definindo o deslocamento máximo do motor a 1000cc. MotoGP foi administrado pelos proprietários dos direitos comerciais da Dorna Sports, sob a supervisão da FIM desde 1992.
